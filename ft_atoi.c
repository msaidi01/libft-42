#include "libft.h"

int	ft_atoi(const char *n)
{
	int		i;
	long	r;
	int		s;

	i = 0;
	r = 0;
	s = 1;
	if (n[i] == '\0')
		return (0);
	while (n[i] == 32 || (n[i] >= 9 && n[i] <= 13))
		i++;
	if (n[i] == '+' || n[i] == '-')
	{
		if (n[i] == '-')
			s *= (-1);
		i++;
	}
	while (n[i] >= '0' && n[i] <= '9')
	{
		r = r * 10;
		r = r + n[i] - '0';
		i++;
	}
	return (r * s);
}
